/**
 * Returns the CiP number and key skill number from a legacy ID like "CiP_11_KS03".
 */
export function parseLegacyKeySkillId(legacyId: string): {
  cipNumber: number;
  skillNumber: number;
} | null {
  const match = legacyId.match(/^CiP_(\d+)_KS(\d+)$/);
  if (!match) return null;
  return {
    cipNumber: parseInt(match[1], 10),
    skillNumber: parseInt(match[2], 10),
  };
}
